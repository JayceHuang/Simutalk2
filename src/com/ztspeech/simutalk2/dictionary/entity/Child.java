package com.ztspeech.simutalk2.dictionary.entity;


public class Child {

	private Integer childId;
	private Integer categroyId;
	private String childName;
	private Integer childHeat;
	public Integer getChildId() {
		return childId;
	}
	public void setChildId(Integer childId) {
		this.childId = childId;
	}
	public Integer getCategroyId() {
		return categroyId;
	}
	public void setCategroyId(Integer categroyId) {
		this.categroyId = categroyId;
	}
	public String getChildName() {
		return childName;
	}
	public void setChildName(String childName) {
		this.childName = childName;
	}
	public Integer getChildHeat() {
		return childHeat;
	}
	public void setChildHeat(Integer childHeat) {
		this.childHeat = childHeat;
	}

}
